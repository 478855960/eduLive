package com.yiming.entity;

public class WhiteboardData {
    String type, color, oldX, oldY, curX, curY;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getOldX() {
        return oldX;
    }

    public void setOldX(String oldX) {
        this.oldX = oldX;
    }

    public String getOldY() {
        return oldY;
    }

    public void setOldY(String oldY) {
        this.oldY = oldY;
    }

    public String getCurX() {
        return curX;
    }

    public void setCurX(String curX) {
        this.curX = curX;
    }

    public String getCurY() {
        return curY;
    }

    public void setCurY(String curY) {
        this.curY = curY;
    }
}
